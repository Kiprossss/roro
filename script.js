let currentFriend = null;
const friendsDiv = document.getElementById("friends");

friends.forEach((friend, index) => {
  const div = document.createElement("div");
  div.className = "friend";
  div.innerHTML = `
    <img src="${friend.pfp}" class="pfp">
    <span>${friend.name}</span>
  `;
  div.onclick = () => openChat(index);
  friendsDiv.appendChild(div);
});

function hideAll() {
  ["home","chat","gallery","birthday"].forEach(id =>
    document.getElementById(id).classList.add("hidden")
  );
}

function openChat(index) {
  currentFriend = friends[index];
  hideAll();
  document.getElementById("chat").classList.remove("hidden");

  chatName.innerText = currentFriend.name;
  chatMessage.innerText = currentFriend.message;
  chatPfp.src = currentFriend.pfp;
}

function goHome() {
  hideAll();
  home.classList.remove("hidden");
  closeVideo();
}

function goBackToChat() {
  hideAll();
  chat.classList.remove("hidden");
  closeVideo();
  bdayAudio.pause();
}

function showVideo() {
  const container = document.getElementById("videoContainer");
  const video = document.getElementById("videoPlayer");

  video.pause();
  video.removeAttribute("src");
  video.load();

  video.src = currentFriend.video;
  video.load();

  container.classList.add("show");
  container.classList.remove("hidden");

  video.play();
}

function closeVideo() {
  const container = document.getElementById("videoContainer");
  const video = document.getElementById("videoPlayer");

  video.pause();
  video.currentTime = 0;
  container.classList.remove("show");
  container.classList.add("hidden");
}

function openGallery() {
  hideAll();
  gallery.classList.remove("hidden");

  const galleryEl = document.getElementById("galleryImages");
  galleryEl.innerHTML = "";

  currentFriend.gallery.forEach(img => {
    const image = document.createElement("img");
    image.src = img;
    image.onclick = () => window.open(img, "_blank");
    galleryEl.appendChild(image);
  });
}

function openBirthday() {
  hideAll();
  birthday.classList.remove("hidden");

  document.getElementById("birthdayText").innerText =
    currentFriend.birthday;

  bdayAudio.currentTime = 0;
  bdayAudio.play();
}

